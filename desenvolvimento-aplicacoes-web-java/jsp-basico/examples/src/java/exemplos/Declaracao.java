package exemplos;

/**
 *
 * @author chiquitto
 */
public class Declaracao {
  public static void main(String[] args) {
    boolean verdadeiro = true;
    boolean falso = false;
    
    System.out.println(verdadeiro);
    System.out.println(falso);
    
    byte byteNegativo = -8;
    byte bytePositivo = 8;
    
    System.out.println(byteNegativo);
    System.out.println(bytePositivo);
    
    short shortNegativo = -16;
    short shortPositivo = 16;
    
    System.out.println(shortNegativo);
    System.out.println(shortPositivo);
    
    int intNegativo = -32;
    int intPositivo = 32;
    
    System.out.println(intNegativo);
    System.out.println(intPositivo);
    
    long longNegativo = -64;
    long longPositivo = 64;
    
    System.out.println(longNegativo);
    System.out.println(longPositivo);
    
    float float1 = -3.1415f;
    float float2 = 3.1415f;
    
    System.out.println(float1);
    System.out.println(float2);
    
    double double1 = -3.14151617;
    double double2 = 6.28303234;
    
    System.out.println(double1);
    System.out.println(double2);
    
    char charA1 = '\u0041'; // A
    char charA2 = 65; // A
  }
}
